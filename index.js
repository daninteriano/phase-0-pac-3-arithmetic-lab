function add(num1, num2){
    let sum = num1 + num2
    return sum
}
function subtract(sub1, sub2){
    let total = sub1 - sub2
    return total
}
function multiply(mult1, mult2){
    let star = mult1 * mult2
    return star
}
function divide(div1, div2){
    let bar = div1 / div2
    return bar
}
